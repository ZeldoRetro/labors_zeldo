local map = ...
local game = map:get_game()

local light_img = sol.surface.create(320,240)
light_img:fill_color({255, 255, 255})
local light = false

map:register_event("on_draw",function(map,dst_surface)
  if light then light_img:draw(dst_surface) end
end)

function lit_up_1:on_activated()
  self:set_enabled(false)
  torch_step_1_1:set_lit(true)
  torch_step_1_2:set_lit(true)
  sol.audio.play_sound("lamp")
end

function lit_up_2:on_activated()
  self:set_enabled(false)
  torch_step_2_1:set_lit(true)
  torch_step_2_2:set_lit(true)
  sol.audio.play_sound("lamp")
end

-- GANON APPARAIT
function ganon_sensor:on_activated()
  self:set_enabled(false)
  hero:freeze()
  sol.audio.play_music("none")
  ganon_npc:set_enabled(true)
  ganon_npc:get_sprite():set_animation("warping")
  sol.audio.play_sound("ganon_tp")
  local camera = map:get_camera()
  local camera_movement = sol.movement.create("target")
  camera_movement:set_ignore_obstacles(true)
  camera_movement:set_target(camera:get_position_to_track(ganon_npc))
  camera_movement:set_speed(128)
  camera_movement:start(camera, function()
    ganon_npc:get_sprite():set_animation("spell")
    sol.audio.play_sound("laser")
    local m = sol.movement.create("target")
    m:set_speed(144)
    m:set_target(ganon_npc)
    m:start(triforce_flying,function()
      m:stop()
      light = true
      light_img:fade_in(5,function()
        triforce_flying:set_enabled(false)
        ganon_npc:get_sprite():set_animation("stopped")
        light_img:fade_out(5,function()
          sol.timer.start(map, 2000, function()
            game:set_dialog_position("bottom")
            game:start_dialog("LABORS.1st_solarus_quest.ganon_alt",function()
              game:set_dialog_position("auto")
              local camera_movement = sol.movement.create("target")
              camera_movement:set_ignore_obstacles(true)
              camera_movement:set_target(camera:get_position_to_track(hero))
              camera_movement:set_speed(256)
              camera_movement:start(camera, function()
                camera:start_tracking(hero)
                hero:unfreeze()
                sol.audio.play_music("creations/labors/1st_solarus_quest/final_ganon")
                ganon_npc:get_sprite():set_animation("warping")
                sol.audio.play_sound("ganon_tp")
                sol.timer.start(map, 500, function()
                  map:set_entities_enabled("battle_ground_", true)
                  map:set_entities_enabled("final_ganon_", true)
                  map:set_entities_enabled("carpet", false)
                  ganon_npc:set_enabled(false)
                end)
              end)
            end)
          end)
        end)
      end)    
    end)
  end)
end

-- COMBAT FINAL CONTRE GANON : REND LA TRIFORCE QUAND TOUS VAINCUS
for enemy in map:get_entities("final_ganon_") do
  function enemy:on_dead()
    if not map:has_entities("final_ganon_") then
      sol.audio.play_music("triforce_chamber")
      map:set_entities_enabled("triforce_final_step_",true)
      local m1 = sol.movement.create("target")
      m1:set_speed(24)
      m1:set_target(target_triforce_final_step_1)
      m1:start(triforce_final_step_1)
      local m2 = sol.movement.create("target")
      m2:set_speed(24)
      m2:set_target(target_triforce_final_step_3)
      m2:start(triforce_final_step_3)
      local m3 = sol.movement.create("target")
      m3:set_speed(24)
      m3:set_target(target_triforce_final_step_2)
      m3:start(triforce_final_step_2,function()
        final_triforce_sensor:set_enabled(true)
      end)       
    end
  end
end

-- TRIFORCE OBTENUE ET FIN
function final_triforce_sensor:on_activated()
  self:set_enabled(false)
  hero:freeze()
  game:set_pause_allowed(false)
  game:set_life(game:get_max_life())
  game:set_magic(game:get_max_magic())
  local x, y = hero:get_position()
  local m1 = sol.movement.create("target")
  m1:set_speed(16)
  m1:set_target(x, y - 16)
  m1:start(triforce_final_step_1)
  local m2 = sol.movement.create("target")
  m2:set_speed(16)
  m2:set_target(x, y - 16)
  m2:start(triforce_final_step_3)
  local m3 = sol.movement.create("target")
  m3:set_speed(16)
  m3:set_target(x, y - 16)
  m3:start(triforce_final_step_2,function()
    sol.audio.play_sound("ok")
      light = true
      light_img:fade_in(5,function()
        triforce_final_step_1:set_enabled(false)
        triforce_final_step_2:set_enabled(false)
        triforce_final_step_3:set_enabled(false)
        triforce_flying:set_enabled(true)
        sol.audio.play_music("triforce_obtained",false)
        triforce_flying:set_position(x, y - 24)
        hero:set_direction(3)
        hero:set_animation("brandish")
        light_img:fade_out(5,function()
          sol.timer.start(map, 3000, function()
            triforce_flying:set_enabled(false)
            hero:set_animation("stopped")
            light_img:fade_in(100,function()
              game:set_pause_allowed(true)
              game:set_value("labors_wave_2_2_done",true)
              hero:teleport("creations/labors/1st_solarus_quest/hub","start_final","fade")
            end)
          end)
        end)
      end)
  end) 
end